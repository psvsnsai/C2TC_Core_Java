package com.tns.classsessions;

public class StringConcat {

	public static void main(String[] args) {
		//initializing string
		String s="Tanuku ";
		s=s.concat("Bhimavaram "); //concatenating with Bhimavaram
		System.out.println(s);
		
		//concatenating with AP 
		s= s+"AP";
		System.out.println(s);
		
		s+=" India";//   concatenating with India  
		System.out.println(s);

	}

}
